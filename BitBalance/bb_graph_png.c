{
    if (ppgp && *ppgp)
    {
        if ((*ppgp)->png)
            libattopng_destroy((*ppgp)->png);
        if ((*ppgp)->r)
            free((*ppgp)->r);
        if ((*ppgp)->buf)
            free((*ppgp)->buf);
        free(*ppgp);
        *ppgp = NULL;
    }
}

