int bb_png_draw_rect(struct bb_png_data *ppd, int x, int y, int w, int h, int color_n, struct bb_error *perr)
{
    struct bb_paste_image pid = {0};
    char *p, *p2;
    int err_code;

    err_code = 0;

    if (!ppd)
        err_code = 3;
    else if (!w || !h)
        err_code = 7;
    else if ((color_n < 0) || (color_n >= BB_COLORS_N))
        err_code = 8;
    else
    {
        bb_get_ppi(w, h, ppd->png->data, ppd->w, ppd->h, x, y, &pid);

        for (p = pid.i_start; p < pid.i_stop; p += pid.i_step)
            for (p2 = p + pid.i_string; p < p2; )
                *p++ = (char) color_n;
    }

    if (err_code && perr)
        set_error(err_code, perr);
    return err_code;
}

int bb_png_write_text(struct bb_png_data *ppd, int x, int y, const char *text, size_t text_len, _Bool rotate, struct bb_error *perr)
{
    const char *p, *p2;
    size_t n;
    int err_code;

    err_code = 0;

    //puts("---");
    if (!ppd || !text)
        err_code = 3;
    else
    {
        for (p = text, p2 = p + text_len; (p < p2) && !err_code; ++p)
        {
            if (((*p < 48) || (*p > 57)) &&         // 0-9
                ((*p < 65) || (*p > 90)) &&         // A-Z
                ((*p < 97) || (*p > 122)) &&        // a-z
                (*p != 44) &&                       // ,
                (*p != 45) &&                       // -
                (*p != 46))                         // .
            {
                err_code = 9;
                //printf("%zu\n", ((size_t) *p) & 0xff);
                //printf("%zu\n", (p - text));
            }
            else
            {
                n = ((size_t) *p) & 0xff;
                if ((n > 47) && (n < 58))
                    n += 4;
                else if ((n > 64) && (n < 91))
                    n -= 65;
                else if ((n > 96) && (n < 123))
                    n -= 71;
                else if ((44 == n) || (46 == n))
                    n = 62;
                else                    // if (45 == n)
                    n = 63;
                //printf("%zu\n", n);

                if (!rotate)
                {
                    bb_paste_ti(BB_LETTERS + n * BB_LETTER_W * BB_LETTER_H, BB_LETTER_W, BB_LETTER_H, 
                        ppd->png->data, ppd->w, ppd->h, x, y);
                    x += BB_LETTER_W;
                }
                else
                {
                    bb_paste_ti(ppd->r + n * BB_LETTER_H * BB_LETTER_W, BB_LETTER_H, BB_LETTER_W, 
                        ppd->png->data, ppd->w, ppd->h, x, y - BB_LETTER_W);
                    y -= BB_LETTER_W;
                }
            }
        }
    }
    //puts("---");

    if (err_code && perr)
        set_error(err_code, perr);
    return err_code;
}

int bb_png_save(struct bb_png_data *ppd, const char *file_name, struct bb_error *perr)
{
    return ((!ppd || !file_name) ? 3 : (libattopng_save(ppd->png, file_name) ? 10 : 0));
}

void bb_png_free(struct bb_png_data **pppd)
{
    if (pppd && *pppd)
    {
        if ((*pppd)->png)
            libattopng_destroy((*pppd)->png);
        if ((*pppd)->r)
            free((*pppd)->r);
        free(*pppd);
        *pppd = NULL;
    }
}

const char * bb_png_error_message(int err_code)
{
    static const char *BB_PNG_ERRS[BB_PNG_ERRS_N] = {   "",                                                       //  0
                                                        "  ",                                  //  1
                                                        " ",                                       //  2
                                                        " ",                                         //  3
                                                        "     .",     //  4
                                                        "   png",                     //  5
                                                        "    png",                         //  6
                                                        "     .",  //  7
                                                        "   .",                      //  8
                                                        "   ",                             //  9
                                                        "   png-",                          // 10
    };

    if ((err_code < 0) || (err_code >= BB_PNG_ERRS_N))
        err_code = 2;

    return *(BB_PNG_ERRS + err_code);
}

static void bb_paste_ti(const char *t_i, int t_w, int t_h, char *image, int w, int h, int pen_x, int pen_y)
{
    struct bb_paste_image pid = {0};
    char *p, *p2;
    const char *pt;

    bb_get_ppi(t_w, t_h, image, w, h, pen_x, pen_y, &pid);

    for (p = pid.i_start, pt = t_i + pid.to_start; p < pid.i_stop; p += pid.i_step, pt += pid.t_step)
        for (p2 = p + pid.i_string; p < p2; )
            *p++ = *pt++;
}

static void bb_get_ppi(int t_w, int t_h, char *image, int w, int h, int pen_x, int pen_y, struct bb_paste_image *ppi)
{
    if (pen_x < 0)
    {
        if (pen_y < 0)
        {
            ppi->i_start = image;
            ppi->to_start = -(pen_y * t_w + pen_x);
        }
        else
        {
            ppi->i_start = image + (pen_y * w);
            ppi->to_start = -pen_x;
        }

        ppi->i_string = (t_w + pen_x);
        if (ppi->i_string > w)
            ppi->i_string = w;
    }
    else
    {
        if (pen_y < 0)
        {
            ppi->i_start = image + pen_x;
            ppi->to_start = -pen_y * t_w;
        }
        else
        {
            ppi->i_start = image + pen_y * w + pen_x;
            ppi->to_start = 0;
        }

        ppi->i_string = t_w;
        if (ppi->i_string > w - pen_x)
            ppi->i_string = w - pen_x;
    }

    ppi->i_step = w - ppi->i_string;
    ppi->t_step = t_w - ppi->i_string;
    ppi->i_stop = (t_h + pen_y > h) ? image + w * h : image + w * (t_h + pen_y);
}

static void set_error(int err_code, struct bb_error *perr)
{
    perr->err_code = err_code;
    perr->err_str = bb_png_error_message(err_code);
}